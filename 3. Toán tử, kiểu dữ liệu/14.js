// Làm quen với toán tử

/**
 * Toán tử trong JavaScript
 * 
 * 1. Toán tử số học- arithmetic( các phép tính +; -; *; **; /; %; ++; --)
 * 2. Toán tử gán- assignment( =; +=; -=; *=; /=; **=)
 * 3. Toán tử so sánh- comparison( ==; !=; >; <; >=; <=; so sánh tuyệt đối ===; !==)
 * 4. Toán tử logic- logical( &&; ||; !)
 */

// Toán tử số học
var a= 1+ 2;
console.log(a)

// Toán tử gán
var a= 1+ 2;

// Toán tử so sánh
var a= 1;
var b= 2;
if(a==b) {
    console.log('Đúng')
} else {
    console.log('Sai')
}

// Toán tử logic
var a= 1;
var b= 2;
