// Toán tử số học

/**
+       --> Cộng
-       --> Trừ
*       --> Nhân
**      --> Lũy thừa
/       --> Chia
%       --> Chia lấy số dư
++      --> Tăng một giá trị số
--      --> Giảm một giá trị số
*/

// Toán tử +
var a= 1;
var b= 2;
var c= a+b;
console.log(c)

// Toán tử -
var a= 1;
var b= 2;
var c= a-b;
console.log(c)

// Toán tử *
var a= 1;
var b= 2;
var c= a*b;
console.log(c)

// Toán tử **
var a= 1;
var b= 2;
var c= a**b;
console.log(c)

// Toán tử /
var a= 1;
var b= 2;
var c= a/b;
console.log(c)

// Toán tử %
var a= 1;
var b= 2;
var c= a%b;
console.log(c)

// Toán tử ++
var a= 1;
a ++;
console.log(a)

// Toán tử --
var a= 1;
a --;
console.log(a)