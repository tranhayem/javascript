// Lưu ý khi học lập trình tạp F8

/**
 * Cách hoàn thành bài học video?

 * Xem hết nội dung video là sẽ hoàn thành bài học
 * Tắt extension chặn quảng cáo (VD adsblock) vì có thể gây xung đột (F8 không hề có quảng cáo)
 * Xem video ở tốc độ vừa phải, tua quá nhanh hoặc để tốc độ quá nhanh có thể không hoàn thành được bài học
 */

/**
 * Cách hoàn thành bài học text?

 * Bạn cần đọc hết nội dung, cuộn xuống dưới cùng để hoàn thành bài
 * Nếu cuộn xuống quá nhanh, có thể bạn sẽ không hoàn thành được bài học
 */

/**
 * Tại sao bài học lại bị khóa?

 * Giúp người mới học tập đúng lộ trình một cách bài bản
 * Cấp chứng chỉ hoàn thành khóa học cho bạn 🎉🎉
 */

/**
 * Bài kiểm tra là gì?

 * Tại F8, bạn có thể thực hành sau mỗi bài học ngay tại trang web này, mỗi bài thực hành có thể có những bài kiểm tra. Các bài kiểm tra được đưa ra nhằm đảm bảo code của bạn đã đạt yêu cầu.
 */